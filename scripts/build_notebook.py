"""Build a Jupyter notebook by combining a markdown documentation file with
a source notebook.

The generated notebook is structured as:

  cell 0 : markdown  -- the documentation (with a Binder badge)
  cell 1+ : cells from the source notebook (cell type preserved)

No external dependencies (no nbformat). The notebook is hand-rolled as JSON.
"""

import argparse
import json
import os
import re
from urllib.parse import quote


MARKER = "<!-- AUTOGENERATED-DOCS -->"


def build_binder_url(repo: str, branch: str, notebook_path: str) -> str:
    encoded_path = quote(notebook_path, safe="")
    return (
        f"https://mybinder.org/v2/gh/{repo}/{branch}"
        f"?labpath={encoded_path}"
    )


def resolve_image_paths(doc_text: str, doc_dir: str, repo: str, branch: str) -> str:
    def replacer(match):
        alt = match.group(1)
        rel_path = match.group(2)
        if rel_path.startswith(("http://", "https://")):
            return match.group(0)
        abs_path = os.path.normpath(os.path.join(doc_dir, rel_path))
        repo_rel = os.path.relpath(abs_path, start=".")
        encoded = quote(repo_rel, safe="/")
        url = f"https://raw.githubusercontent.com/{repo}/{branch}/{encoded}"
        return f"![{alt}]({url})"

    return re.sub(r"!\[([^\]]*)\]\(([^)]+)\)", replacer, doc_text)


def make_markdown_cell(source: str) -> dict:
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": source.splitlines(keepends=True),
    }


def extract_cells_from_notebook(source_notebook_path: str) -> list:
    """Return a list of notebook cells from an existing .ipynb file.

    Each cell is a dict with at least ``cell_type`` and ``source``.
    Outputs and execution counts are cleared so the output notebook is clean.
    """
    with open(source_notebook_path, "r", encoding="utf-8") as f:
        nb = json.load(f)

    cells = []
    for cell in nb.get("cells", []):
        clean = {
            "cell_type": cell["cell_type"],
            "metadata": cell.get("metadata", {}),
            "source": cell.get("source", []),
        }
        if cell["cell_type"] == "code":
            clean["execution_count"] = None
            clean["outputs"] = []
        cells.append(clean)
    return cells


def build_notebook(
    doc_path: str,
    source_notebook_path: str,
    notebook_path: str,
    repo: str,
    branch: str,
) -> dict:
    with open(doc_path, "r", encoding="utf-8") as f:
        doc_text = f.read()

    doc_dir = os.path.dirname(os.path.abspath(doc_path))
    doc_text = resolve_image_paths(doc_text, doc_dir, repo, branch)

    binder_url = build_binder_url(repo, branch, notebook_path)
    badge_md = f"[![Binder](https://mybinder.org/badge_logo.svg)]({binder_url})"
    doc_cell_source = f"{MARKER}\n{badge_md}\n\n{doc_text}"

    cells = [make_markdown_cell(doc_cell_source)]
    cells.extend(extract_cells_from_notebook(source_notebook_path))

    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            },
            "language_info": {"name": "python", "version": "3.12"},
        },
        "nbformat": 4,
        "nbformat_minor": 5,
    }


def main() -> None:
    parser = argparse.ArgumentParser(
        description=(
            "Build a Jupyter notebook by combining a markdown documentation "
            "file with a source notebook."
        )
    )
    parser.add_argument("--doc", required=True, help="Path to the markdown doc file.")
    parser.add_argument(
        "--source-notebook",
        required=True,
        help="Path to the source notebook whose cells are appended.",
    )
    parser.add_argument(
        "--notebook",
        required=True,
        help="Path to the output notebook file (will be overwritten).",
    )
    parser.add_argument(
        "--repo",
        default="Simulation-Benchmarks/NFDI4IngModelValidationPlatform",
        help="GitHub owner/repo (used in the Binder badge).",
    )
    parser.add_argument("--branch", default="main", help="Git branch name.")
    args = parser.parse_args()

    nb = build_notebook(
        doc_path=args.doc,
        source_notebook_path=args.source_notebook,
        notebook_path=args.notebook,
        repo=args.repo,
        branch=args.branch,
    )

    notebook_dir = os.path.dirname(os.path.abspath(args.notebook))
    os.makedirs(notebook_dir, exist_ok=True)

    with open(args.notebook, "w", encoding="utf-8") as f:
        json.dump(nb, f, ensure_ascii=False, indent=1)
        f.write("\n")


if __name__ == "__main__":
    main()
